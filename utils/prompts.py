def format_tz(
    goal: str,
    audience: str,
    scope_in: str,
    scope_out: str,
    constraints: str,
    deliverables: str,
    success_criteria: str,
    open_questions: str
) -> str:
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∏—Ç–æ–≥–æ–≤–æ–µ –¢–ó –ø–æ —à–∞–±–ª–æ–Ω—É"""
    return TZ_TEMPLATE.format(
        goal=goal,
        audience=audience,
        scope_in=scope_in,
        scope_out=scope_out,
        constraints=constraints,
        deliverables=deliverables,
        success_criteria=success_criteria,
        open_questions=open_questions
    )


def format_red_flags(risks: list[str]) -> str:
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –±–ª–æ–∫ —Ä–∏—Å–∫–æ–≤"""
    if not risks:
        return ""
    risks_text = "\n".join(f"‚Ä¢ {risk}" for risk in risks)
    return RED_FLAGS_TEMPLATE.format(risks=risks_text)


